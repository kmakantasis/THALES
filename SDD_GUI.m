% --- Executes on button press in run_button.
function run_button_Callback(hObject, eventdata, handles)
% hObject    handle to run_button (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

    discretized_values = DiscretizeCriteriaGui(handles.criteria, 1, 0, -1);
    
    if length(discretized_values) ~= 0
        ranking = DDSSGui(handles.criteria);
        if ranking > 1.0
            ranking = 1.0;
        end
        if ranking < -1.0
            ranking = -1.0;
        end
        set(handles.score,'String',num2str(ranking))
    
        if ranking < -.033
            ranking_tag = 'BAD';
        elseif ranking >= -0.33 && ranking < 0.33
            ranking_tag = 'MEDIUM';
        else
            ranking_tag = 'GOOD';
        end
        set(handles.ranking,'String',ranking_tag);
    end
    
    



